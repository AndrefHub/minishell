void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	size_t	i;
	size_t	len;

	if (s && f)
	{
		i = -1;
		len = ft_strlen(s);
		while (++i < len)
			f(i, s + i);
	}
}
